package assignments.assigment05_day12_forLoop;

public class Q10_numberPyramid {

	public static void main(String[] args) {
		
		
        int rows=7;
		
		
		for(int i=1;i<=rows;i++) {
			
			for(int j=1;j<=i;j++) {
				
				System.out.print(i);
			
			}
			System.out.println();
			
			}
		
		
		
		

	}

}
