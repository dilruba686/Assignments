public class Q12_nestedFor {

	public static void main(String[] args) {
		
		
		
		
		
	for(int i=1;i<=10;i++) {
			
		for(int a=i;a<=i*10;a+=i) {
			System.out.print(a+" ");
	         
		}
			
			System.out.println();
		
			
		}
		

		
	
	}

}
